<template>
    <div>

        <div
                v-if="server_message !== false"
                class="alert alert-danger"
                role="alert"
        >
            {{ this.server_message }}
            <a v-if="try_logging_in" href="/login">Login</a>
        </div>

        <div>
            <form @submit.prevent="handleSubmit" class="form-horizontal">
                <input type="hidden" ref="newCharge">
                <div class="row">

                    <div class="col-md-3">
                        <std-form-group
                                label="Convicted"
                                label-for="convicted"
                                :errors="form_errors.convicted"
                        >
                            <fld-convicted name="convicted" v-model="charge.convicted"/>
                        </std-form-group>
                    </div>

                    <div class="col-md-3">
                        <std-form-group
                                label="Eligible"
                                label-for="eligible"
                                :errors="form_errors.eligible"
                        >
                            <fld-eligible name="eligible" v-model="charge.eligible"/>
                        </std-form-group>
                    </div>

                    <div class="col-md-3">
                        <std-form-group
                                label="Please Expunge"
                                label-for="please_expunge"
                                :errors="form_errors.please_expunge"
                        >
                            <fld-expunge
                                    name="please_expunge"
                                    v-model="charge.please_expunge"
                            />
                        </std-form-group>
                    </div>

                    <div class="col-md-2">

                    </div>

                    <div class="col-md-1">
                        <slot></slot>
                    </div>
                </div>

                <div class="row">
                    <div class="col-md-2">
                        <std-form-group
                                label="Citation"
                                label-for="citation"
                                :errors="form_errors.citation"
                        >
                            <fld-statute
                                v-model="charge.citation"
                                @selected="statuteSelected"
                            ></fld-statute>
                        </std-form-group>
                    </div>

                    <div class="col-md-10">
                        <std-form-group
                                label="Charge"
                                label-for="charge"
                                :errors="form_errors.charge"
                        >
                            <fld-input name="charge" v-model="charge.charge"/>
                        </std-form-group>
                    </div>

                </div>


                <div class="row">
                    <div class="col-md-2">
                        <std-form-group
                                label="Charge Type"
                                label-for="conviction_charge_type"
                                :errors="form_errors.conviction_charge_type"
                        >
                            <fld-charge-type
                                    name="conviction_charge_type"
                                    v-model="charge.conviction_charge_type"
                            />
                        </std-form-group>
                    </div>
                    <div class="col-md-2">
                        <std-form-group
                                label="Class"
                                label-for="conviction_class_type"
                                :errors="form_errors.conviction_class_type"
                        >
                            <fld-charge-class
                                    name="conviction_class_type"
                                    v-model="charge.conviction_class_type"
                            />
                        </std-form-group>

                    </div>
                    <div class="col-md-8">
                        <std-form-group
                                label="Sentence"
                                label-for="sentence"
                                :errors="form_errors.sentence"
                        >
                            <fld-input name="sentence" v-model="charge.sentence"/>
                        </std-form-group>
                    </div>


                </div>

                <div class="row">
                    <div class="col-md-12">
                        <std-form-group
                                label="Notes"
                                label-for="notes"
                                :errors="form_errors.notes"
                        >
                            <fld-text-area name="notes" v-model="charge.notes" rows="5"/>
                        </std-form-group>
                    </div>
                    <div class="col-md-12">
                        <std-form-group
                                label="Reason for Change"
                                label-for="reason_for_change"
                                :errors="form_errors.reason_for_change"
                        >
                            <fld-text-area
                                    name="reason_for_change"
                                    v-model="charge.reason_for_change"
                                    required
                                    rows="5"
                            />
                        </std-form-group>
                    </div>
                </div>


                <div class="form-group mt-4">
                    <div class="row">
                        <div class="col-md-4 text-md-left mt-2 mt-md-0">
                            <button class="btn btn-secondary" @click.prevent="cancel">Cancel</button>
                        </div>
                        <div class="col-md-4 text-center mt-2 mt-md-0" v-if="charge.id > 0">
                            <button class="btn btn-danger" @click.prevent="deleteCharge">Delete Charge</button>
                        </div>
                        <div class="col-md-4 text-md-right">
                            <button
                                    type="submit"
                                    class="btn btn-primary"
                                    :disabled="processing"
                            >
                                Save
                            </button>
                        </div>
                    </div>
                </div>
            </form>
        </div>

    </div>
</template>

<script>

    export default {
        name: "charge-edit",
        props: {
            charge: {
                type: [Boolean, Object],
                default: false
            }
        },
        data() {
            return {
                form_errors: {
                    id: false,
                    conviction_id: false,
                    charge: false,
                    citation: false,
                    conviction_class_type: false,
                    conviction_charge_type: false,
                    sentence: false,
                    convicted_text: false,
                    eligible_text: false,
                    please_expunge_text: false,
                    to_print: false,
                    notes: false,
                    convicted: false,
                    eligible: false,
                    please_expunge: false
                },
                server_message: false,
                try_logging_in: false,
                processing: false,
                isShowing: false,
                backup_copy: {},
                statutes: [],
            };
        },
        mounted() {
            this.getStatutes()
        },
        created() {

            /// make back up copy
            for (let index in this.charge) {
                this.backup_copy[index] = this.charge[index]
            }
        },
        computed: {

            dsp_convicted() {
                let q = this.charge.convicted;
                return parseInt(q) ? ' -- Convicted' : '';
            },
            dsp_eligible() {
                let q = this.charge.eligible;
                return parseInt(q) ? ', Eligible' : '';
            },
            dsp_please_expunge() {
                let q = this.charge.please_expunge;
                return parseInt(q) ? ', PleaseExpunge' : '';
            },
        },
        methods: {
            async handleSubmit() {
                let $this = this
                this.server_message = false;
                this.processing = true;
                let url = "";
                let amethod = "";
                if (this.charge.id) {
                    url = "/charge/" + this.charge.id;
                    amethod = "put";
                } else {
                    url = "/charge";
                    amethod = "post";
                }
                await axios({
                    method: amethod,
                    url: url,
                    data: this.charge
                })
                    .then(res => {
                        if (res.status === 200) {
                            // if saved set the get the id back and set to instance
                            if (res.data.charge) {
                                /// set id in case this is a new entry
                                $this.charge.id = res.data.charge.id
                            }
                            /// reset reason for change
                            $this.charge.reason_for_change = ''
                            /// recopy the new charge to our backup
                            for(let index in $this.charge) {
                                $this.backup_copy[index] = $this.charge[index]
                            }

                            $this.processing = false
                            $this.$bus.$emit('minimize-charge:charge:'+$this.charge.id)
                        } else {
                            this.server_message = res.status;
                        }
                    })
                    .catch(error => {
                        if (error.response) {
                            if (error.response.status === 422) {
                                // Clear errors out
                                Object.keys(this.form_errors).forEach(
                                    i => (this.form_errors[i] = false)
                                );
                                // Set current errors
                                Object.keys(error.response.data.errors).forEach(
                                    i =>
                                        (this.form_errors[i] =
                                            error.response.data.errors[i])
                                );
                            } else if (error.response.status === 404) {
                                // Record not found
                                this.server_message = "Record not found";
                                window.location = "/charge";
                            } else if (error.response.status === 419) {
                                // Unknown status
                                this.server_message =
                                    "Unknown Status, please try to ";
                                this.try_logging_in = true;
                            } else if (error.response.status === 500) {
                                // Unknown status
                                this.server_message =
                                    "Server Error, please try to ";
                                this.try_logging_in = true;
                            } else {
                                this.server_message = error.response.data.message;
                            }
                        } else {
                            console.log(error.response);
                            this.server_message = error;
                        }
                        this.processing = false;
                    });
            },
            deleteCharge() {
                let $this = this
                if (confirm('Do you want to delete record?')) {
                    axios.delete(`/charge/${this.charge.id}`)
                    .then(response => {
                        // send delete event to Charges List
                        this.$bus.$emit('charge-deleted:conviction:'+$this.charge.conviction_id, $this.charge.id)
                    })
                    .catch(error => {
                        console.log(error)
                    })
                }
            },
            cancel() {
                console.log('cancel')
                if (this.charge.id === 0) {
                    this.$bus.$emit('charge-deleted:conviction:'+this.charge.conviction_id, this.charge.id)
                } else {
                    for (let index in this.backup_copy) {
                        this.charge[index] = this.backup_copy[index]
                        this.charge.reason_for_change = ''
                    }
                }
            },
            getStatutes() {
                axios.get('/statutes/all')
                    .then(res => {
                        if(res.data) {
                            this.statutes = res.data
                            // this.statute_numbers = this.statutes.map(s => s.number)
                        }
                    })
                    .catch(e => {console.error(e)})
            },
            statuteSelected(statute) {
                this.charge.charge = statute.name;
                this.charge.eligible = statute.statutes_eligibility_id
            }
        }
    };
</script>
